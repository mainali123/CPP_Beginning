#include <iostream>

using namespace std;

int main() {
    int a = 10;
    int b = 5;

    // compound assignment operator +=, -=, *=, /=. %=
    a += 7;


    cout << "Arithmetic Operators" << endl;
    cout << (a + b) << endl;
    cout << (a - b) << endl;
    cout << (a * b) << endl;
    cout << (a / b) << endl;
    cout << (a % b) << endl;

    return 0;
}